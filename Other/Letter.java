package HighSchool.Other;

public class Letter {
    protected int x;
    protected int y;
    protected int w;
    protected int h;
    protected char letter;

    public Letter(int x, int y, int w, int h, char letter) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
        this.letter = letter;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
